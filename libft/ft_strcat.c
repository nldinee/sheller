#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t i;
	size_t c;

	i = ft_strlen(s1);
	c = -1;
	while (s2[++c])
		s1[i++] = s2[c];
	s1[i] = '\0';
	return (s1);
}
